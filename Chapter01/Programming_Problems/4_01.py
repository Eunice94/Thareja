# Write a program to input two numbers and check whether they are equal or not

print('--- A Program to Compare two numbers entered by the user ---')

first_num= int(input('Enter the first number: '))
second_num= int(input('Enter the second number: '))

if first_num == second_num:
    print(f'{first_num} is equal to {second_num}')

else:
    print(f'{first_num} is not equal to {second_num}')
